package traning;

public class bitwiseOperater {

	public static void main(String[] args) {
		int a=5,b=7;
		System.out.println("AND operater:"+ (a&b));
		
		System.out.println("OR  operater:"+ (a|b));
		
		System.out.println("XOR operater:"+ (a^b));
		
		System.out.println("NOR operater:"+ ~ a);
	}

}
